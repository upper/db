package postgresql

import (
	"database/sql"
)

type tx struct {
	*source
	sqlTx *sql.Tx
	done  bool
}

func (self *tx) Commit() (err error) {
	err = self.sqlTx.Commit()
	if err == nil {
		self.done = true
	}
	return err
}

func (self *tx) Rollback() error {
	return self.sqlTx.Rollback()
}
